    private void btnBuscarDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDeleteActionPerformed
        // TODO add your handling code here:
        String texto = "";
        NewJerseyClient cliente = new NewJerseyClient();
        if (txtBuscarDelete.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Error", "Ingrese un registro a eliminar", JOptionPane.ERROR_MESSAGE);
        } else {
            alumnos.Alumnos alumno = cliente.find_XML(alumnos.Alumnos.class, txtBuscarDelete.getText());
            if (alumno != null) {
                texto += "NÃºmero de control: " + alumno.getNumcontrol()
                        + " \nNombre: " + alumno.getNombre()
                        + " \nCarrera: " + alumno.getCarrera();
                cliente.remove(txtBuscarDelete.getText());
                JOptionPane.showMessageDialog(this, texto, "Eliminado", JOptionPane.INFORMATION_MESSAGE);
                texto="";
                alumnos.Alumnoss lista = cliente.findAll_XML(alumnos.Alumnoss.class);
                for (int i = 0; i < lista.getAlumnos().size(); i++) {
                    texto += "NÃºmero de control: " + lista.getAlumnos().get(i).getNumcontrol()
                            + " Nombre: " + lista.getAlumnos().get(i).getNombre()
                            + " Carrera: " + lista.getAlumnos().get(i).getCarrera() + "\n\n";
                }
                txtAreaDelete.setText(texto);
            } else {
                JOptionPane.showMessageDialog(this, txtBuscarDelete.getText(), "No encontrado", JOptionPane.ERROR_MESSAGE);
            }
        }
        cliente.close();
    }//GEN-LAST:event_btnBuscarDeleteActionPerformed

