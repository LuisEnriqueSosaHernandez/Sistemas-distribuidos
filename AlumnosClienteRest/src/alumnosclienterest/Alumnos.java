    private void TablePanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablePanelMouseClicked
        // TODO add your handling code here:
        comboPut.removeAllItems();
        String texto = "";
        NewJerseyClient cliente = new NewJerseyClient();
        alumnos.Alumnoss lista = cliente.findAll_XML(alumnos.Alumnoss.class);
        for (int i = 0; i < lista.getAlumnos().size(); i++) {
            texto += "NÃºmero de control: " + lista.getAlumnos().get(i).getNumcontrol()
                    + " Nombre: " + lista.getAlumnos().get(i).getNombre()
                    + " Carrera: " + lista.getAlumnos().get(i).getCarrera() + "\n\n";
            comboPut.addItem(lista.getAlumnos().get(i).getNumcontrol());
        }
        txtAreaGet.setText(texto);
        txtAreaDelete.setText(texto);
        txtAreaPut.setText(texto);
        cliente.close();
    }//GEN-LAST:event_TablePanelMouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String texto = "";
        NewJerseyClient cliente = new NewJerseyClient();
        if (txtBuscarGet.getText().isEmpty()) {
            alumnos.Alumnoss lista = cliente.findAll_XML(alumnos.Alumnoss.class);
            for (int i = 0; i < lista.getAlumnos().size(); i++) {
                texto += "NÃºmero de control: " + lista.getAlumnos().get(i).getNumcontrol()
                        + " Nombre: " + lista.getAlumnos().get(i).getNombre()
                        + " Carrera: " + lista.getAlumnos().get(i).getCarrera() + "\n\n";
            }
            txtAreaGet.setText(texto);
        } else {
            alumnos.Alumnos alumno = cliente.find_XML(alumnos.Alumnos.class, txtBuscarGet.getText());
            if (alumno != null) {
                texto += "NÃºmero de control: " + alumno.getNumcontrol()
                        + " Nombre: " + alumno.getNombre()
                        + " Carrera: " + alumno.getCarrera();
                txtAreaGet.setText(texto);
            } else {
                JOptionPane.showMessageDialog(this, txtBuscarGet.getText(), "No encontrado", JOptionPane.ERROR_MESSAGE);
            }
        }
        cliente.close();
    }//GEN-LAST:event_btnBuscarActionPerformed

