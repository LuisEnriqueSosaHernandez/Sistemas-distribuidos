    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        if (!txtNumeroControl.getText().isEmpty()) {
            if (!txtNombre.getText().isEmpty()) {
                if (!txtCarrera.getText().isEmpty()) {
                    NewJerseyClient cliente = new NewJerseyClient();
                    alumnos.Alumnos alumno = new alumnos.Alumnos();
                    alumno.setNumcontrol(txtNumeroControl.getText());
                    alumno.setNombre(txtNombre.getText());
                    alumno.setCarrera(txtCarrera.getText());
                    cliente.create_XML(alumno);
                    //System.out.println(cliente.find_XML(alumnos.Alumnos.class, txtNumeroControl.getText()));
                    alumnos.Alumnos alumnoSalida = cliente.find_XML(alumnos.Alumnos.class, txtNumeroControl.getText());
                    JOptionPane.showMessageDialog(this, "NÃºmero de control: " + alumno.getNumcontrol() + "\n"
                            + "Nombre: " + alumno.getNombre() + "\n"
                            + "Carrera: " + alumno.getCarrera() + "\n", "Alumno registrado", JOptionPane.INFORMATION_MESSAGE);
                    cliente.close();
                    txtNumeroControl.setText("");
                    txtNombre.setText("");
                    txtCarrera.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Falta la carrera", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Falta el nombre", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Falta el numero de control", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

